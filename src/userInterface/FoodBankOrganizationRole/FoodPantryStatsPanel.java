    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) mealsServedTable.getModel();
        model.setRowCount(0);
        if (statsMap != null && !statsMap.isEmpty()) {
            Iterator<Entry<String, Integer>> iterator = statsMap.entrySet().iterator();
            while (iterator.hasNext()) {
                Map.Entry<String, Integer> next = (Map.Entry<String, Integer>) iterator.next();
                Object[] row = new Object[2];
                row[0] = next.getKey();
                row[1] = next.getValue();
                model.addRow(row);
            }
        }
    }

    private ArrayList sortDate(ArrayList<String> dateList) {
        Collections.sort(dateList, new Comparator<String>() {
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

            @Override
            public int compare(String o1, String o2) {
                try {
                    return dateFormat.parse(o1).compareTo(dateFormat.parse(o2));
                } catch (ParseException ex) {
                    System.out.println("Date");
                }
                return -1;
            }
        });
        return dateList;
    }

