    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String date = dateFormat.format(jDateChooser.getDate()).toString().trim();
        if (date.equalsIgnoreCase("") || numberjTextField.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Please enter all fields", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int meals = Integer.parseInt(numberjTextField.getText().trim());
        statsMap.put(date, statsMap.containsKey(date) ? statsMap.get(date) + meals : meals);
        populateTable();
        numberjTextField.setText("");
    }//GEN-LAST:event_addButtonActionPerformed

