    private void generateChartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateChartBtnActionPerformed
        // TODO add your handling code here:
        ArrayList<String> keyList = new ArrayList<String>();
        if (statsMap != null && !statsMap.isEmpty()) {
            Set<String> keys = statsMap.keySet();
            for (String key : keys) {
                keyList.add(key);
            }
            Collections.sort(keyList, new Comparator<String>() {
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

                @Override
                public int compare(String o1, String o2) {
                    try {
                        return dateFormat.parse(o1).compareTo(dateFormat.parse(o2));
                    } catch (ParseException ex) {
                        System.out.println("Date");
                    }
                    return -1;
                }
            });
        }

        if (!keyList.isEmpty()) {
            DefaultCategoryDataset defaultCategoryDataset = new DefaultCategoryDataset();
            for (String mealDate : keyList) {
                defaultCategoryDataset.addValue(statsMap.get(mealDate), "Date", mealDate);
            }
            JFreeChart jFreeChart = ChartFactory.createBarChart3D("Pantry Meals Served", "Date", "Number of meals served per day", defaultCategoryDataset, PlotOrientation.VERTICAL, true, true, false);
            CategoryPlot categoryPlot = jFreeChart.getCategoryPlot();
            categoryPlot.setRangeGridlinePaint(Color.white);
            ChartPanel chartPanel = new ChartPanel(jFreeChart);
            chartPanel.setPreferredSize(new java.awt.Dimension(800, 500));
            chartDisplayPanel.removeAll();
            chartDisplayPanel.add(chartPanel);
            chartDisplayPanel.updateUI();
        } else {
            JOptionPane.showMessageDialog(null, "Sorry, you currently do not have any data to display meal stats.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_generateChartBtnActionPerformed

